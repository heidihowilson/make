#!/usr/bin/env node
// sethmakes-icons — CLI fallback for consumers WITHOUT Tailwind v4.
//
// WHY this exists: the Tailwind plugin (@iconify/tailwind4) is the primary path —
// it emits mask-CSS for only the icons you actually use, at YOUR build. Svelte/Remix-v2
// /no-build apps can't use that. This CLI does the same job manually: name some icons,
// get a stylesheet of mask classes you can ship. Same Iconify mask technique, same
// `mk--` aliases, no toolkit republish needed to add an icon.
//
// Usage:
//   sethmakes-icons lucide--check mk--watch ph--television
//   sethmakes-icons mk--watch --out icons.css
//   sethmakes-icons --help
//
// Output: one CSS class per icon, mask-based, sized in `em` so it inherits font-size
// (pair with the --mk-icon-* tokens / .mk-icon helper — see README). Class naming:
//   lucide--check     -> .mk-icon-lucide-check
//   mk--watch         -> .mk-icon-watch        (mk aliases drop the redundant prefix)
//   ph--television    -> .mk-icon-ph-television
// currentColor is preserved, so `color:` styles the glyph.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { createRequire } from 'node:module';
import { getIconData, getIconCSS } from '@iconify/utils';

const require = createRequire(import.meta.url);
const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = join(here, '..');

const HELP = `sethmakes-icons — emit mask-CSS classes for icons (non-Tailwind fallback)

Usage:
  sethmakes-icons <icon...> [--out <file>] [--selector-prefix <str>]

Icons:
  <set>--<name>   e.g. lucide--check, ph--television, mk--watch
                  mk--* are the sethmakes brand aliases (resolved from dist/mk.json).
                  Any installed @iconify-json/<set> set works for <set>.

Options:
  --out <file>           Write CSS to a file instead of stdout.
  --selector-prefix <s>  Class prefix (default: "mk-icon-").
  -h, --help             Show this help.

Examples:
  sethmakes-icons lucide--check mk--watch ph--television
  sethmakes-icons mk--watch mk--play --out app/icons.css
`;

// Lazily load a source set by prefix. mk comes from our own dist/mk.json; everything
// else is resolved from an installed @iconify-json/<prefix> package (same discovery
// the Tailwind plugin uses), so the CLI tracks whatever sets the consumer installed.
const setCache = Object.create(null);
function loadSet(prefix) {
  if (setCache[prefix]) return setCache[prefix];
  let set;
  if (prefix === 'mk') {
    try {
      set = JSON.parse(readFileSync(join(pkgRoot, 'dist', 'mk.json'), 'utf8'));
    } catch {
      throw new Error(
        'dist/mk.json missing — run `npm run build` in @sethmakes/icons first.'
      );
    }
  } else {
    try {
      set = require(`@iconify-json/${prefix}/icons.json`);
    } catch {
      throw new Error(
        `Cannot load set "${prefix}". Install "@iconify-json/${prefix}" as a dependency.`
      );
    }
  }
  setCache[prefix] = set;
  return set;
}

function classNameFor(prefix, name, selectorPrefix) {
  // mk aliases are already branded; don't repeat the prefix in the class.
  return prefix === 'mk'
    ? `${selectorPrefix}${name}`
    : `${selectorPrefix}${prefix}-${name}`;
}

function main(argv) {
  const args = argv.slice(2);
  if (args.length === 0 || args.includes('-h') || args.includes('--help')) {
    process.stdout.write(HELP);
    return 0;
  }

  let outFile = null;
  let selectorPrefix = 'mk-icon-';
  const iconArgs = [];

  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (a === '--out') {
      outFile = args[++i];
      if (!outFile) throw new Error('--out requires a file path');
    } else if (a === '--selector-prefix') {
      selectorPrefix = args[++i];
      if (selectorPrefix == null) throw new Error('--selector-prefix requires a value');
    } else if (a.startsWith('-')) {
      throw new Error(`Unknown option: ${a}`);
    } else {
      iconArgs.push(a);
    }
  }

  if (iconArgs.length === 0) throw new Error('No icons given. See --help.');

  const header =
    '/* Generated by sethmakes-icons. Mask-based icons; `color` styles the glyph.\n' +
    '   Size with the --mk-icon-* tokens or the .mk-icon helper (see @sethmakes/icons README). */\n';
  const blocks = [header];

  for (const icon of iconArgs) {
    const sep = icon.indexOf('--');
    if (sep === -1) {
      throw new Error(`Invalid icon "${icon}" — expected "<set>--<name>".`);
    }
    const prefix = icon.slice(0, sep);
    const name = icon.slice(sep + 2);
    const set = loadSet(prefix);
    const data = getIconData(set, name);
    if (!data) {
      throw new Error(`Icon "${icon}" not found in set "${prefix}".`);
    }
    const selector = '.' + classNameFor(prefix, name, selectorPrefix);
    // mode:'mask' forces the mask technique so currentColor works even for icons
    // that don't self-declare it; em sizing inherits font-size.
    const css = getIconCSS(data, { iconSelector: selector, mode: 'mask' });
    blocks.push(css);
  }

  const output = blocks.join('\n');
  if (outFile) {
    writeFileSync(outFile, output);
    process.stderr.write(`Wrote ${iconArgs.length} icon(s) to ${outFile}\n`);
  } else {
    process.stdout.write(output);
  }
  return 0;
}

try {
  process.exit(main(process.argv));
} catch (err) {
  process.stderr.write(`sethmakes-icons: ${err.message}\n`);
  process.exit(1);
}
